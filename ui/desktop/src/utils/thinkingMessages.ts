/**
 * Get a random thinking message
 * @param enableRandom - Whether to use random messages (default: true)
 * @returns A thinking message string
 */
export function getRandomThinkingMessage(enableRandom: boolean = true): string {
  if (!enableRandom) {
    return 'Thinking';
  }

  const randomIndex = Math.floor(Math.random() * THINKING_MESSAGES.length);
  return THINKING_MESSAGES[randomIndex];
}

/**
 * Get the default thinking message based on the app settings
 * @returns A thinking message string with ellipsis
 */
export async function getThinkingMessage(): Promise<string> {
  try {
    // Check if random thinking messages are enabled (default: true)
    const enableRandom = (await window.appConfig.get('RANDOM_THINKING_MESSAGES')) !== false;
    return `${getRandomThinkingMessage(enableRandom).toLowerCase()}…`;
  } catch {
    // Fallback if config is not available
    return `${getRandomThinkingMessage(true).toLowerCase()}…`;
  }
}
